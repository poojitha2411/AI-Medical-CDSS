def extract_medications(text):
    """Extracts potential medication names from the generated text."""
    # Expanded to include a few more common drugs for better mock extraction
    common_drugs = [
        "ibuprofen", "paracetamol", "amoxicillin", "penicillin", "aspirin",
        "azithromycin", "doxycycline", "ciprofloxacin", "tylenol", "omeprazole",
        "metformin", "lisinopril", "atorvastatin"
    ]
    drugs = []
    # Clean text and split
    words = [re.sub(r'[^a-z]', '', word.lower()) for word in text.split()]
    for word in words:
        if word in common_drugs:
            drugs.append(word)
    return list(set(drugs))

def check_rxnorm_ddi(drugs):
    """
    Screens recommendations via the RxNorm database to detect potential
    drug-drug interactions (Outputs DDI_risk score).
    """
    if len(drugs) < 2:
        return "No DDI risk detected (0-1 drugs identified).", 0.0

    rxcuis = []
    for drug in drugs:
        try:
            # UPDATE: Added 5-second timeout to prevent notebook freezing
            res = requests.get(f"https://rxnav.nlm.nih.gov/REST/rxcui.json?name={drug}", timeout=5)
            if res.status_code == 200:
                data = res.json()
                if "idGroup" in data and "rxnormId" in data["idGroup"]:
                    rxcuis.append(data["idGroup"]["rxnormId"][0])
        except requests.exceptions.RequestException:
            continue # Skip gracefully on API timeout/error

    if len(rxcuis) < 2:
        return "Insufficient RxCUI mappings for interaction check.", 0.0

    cui_list = "+".join(rxcuis)
    try:
        res = requests.get(f"https://rxnav.nlm.nih.gov/REST/interaction/list.json?rxcuis={cui_list}", timeout=5)
        if res.status_code == 200 and "fullInteractionTypeGroup" in res.json():
            # UPDATE: Explicitly returning 1.0 risk score and matching paper terminology
            return "⚠️ FLAGGED FOR PHARMACIST REVIEW: Potential Drug-Drug Interaction detected.", 1.0
    except requests.exceptions.RequestException:
        return "RxNorm API unavailable. Proceed with caution.", 0.0

    return "✅ Passed RxNorm DDI Screening.", 0.0

def check_antibiotic_stewardship(drugs):
    """
    Treatments violating antimicrobial stewardship rules are automatically flagged
    using the NLM RxClass API (Outputs AS_violation score).
    """
    if not drugs:
        return "✅ Passed Antibiotic Stewardship.", 0.0

    flagged_antibiotics = []

    for drug in drugs:
        # Ignore generic safe keywords that might slip through your extractor
        if drug in ["antibiotic", "antibiotics"]:
            flagged_antibiotics.append(drug)
            continue

        try:
            # 1. Get the RxCUI for the drug (Same as your DDI check)
            res = requests.get(f"https://rxnav.nlm.nih.gov/REST/rxcui.json?name={drug}", timeout=5)
            if res.status_code == 200:
                data = res.json()
                if "idGroup" in data and "rxnormId" in data["idGroup"]:
                    rxcui = data["idGroup"]["rxnormId"][0]

                    # 2. Query the RxClass API for the drug's pharmacological class (EPC)
                    class_res = requests.get(
                        f"https://rxnav.nlm.nih.gov/REST/rxclass/class/byRxcui.json?rxcui={rxcui}&relaSource=EPC",
                        timeout=5
                    )

                    if class_res.status_code == 200:
                        class_data = class_res.json()
                        if "rxclassDrugInfoList" in class_data:
                            # 3. Check if any of the classes indicate it is an antibiotic
                            for info in class_data["rxclassDrugInfoList"]["rxclassDrugInfo"]:
                                class_name = info["rxclassMinConceptItem"]["className"].lower()
                                if "anti-bacterial" in class_name or "antibacterial" in class_name or "antibiotic" in class_name:
                                    flagged_antibiotics.append(drug)
                                    break # Found a match, stop checking classes for this drug
        except requests.exceptions.RequestException:
            continue # Skip gracefully on API timeout/error

    if flagged_antibiotics:
        return f"⚠️ FLAGGED: Antibiotic use detected ({', '.join(flagged_antibiotics)}). Enforce stewardship rules.", 1.0

    return "✅ Passed Antibiotic Stewardship.", 0.0

def safety_validation(treatment_plan):
    """Main function to run all safety checks on the generated plan."""
    extracted_drugs = extract_medications(treatment_plan)

    # Calculate DDI_risk and AS_violation penalties
    ddi_msg, ddi_risk = check_rxnorm_ddi(extracted_drugs)
    abx_msg, as_violation = check_antibiotic_stewardship(treatment_plan)

    # UPDATE: If either risk score is triggered, the recommendation is considered unsafe
    is_safe = (ddi_risk == 0.0) and (as_violation == 0.0)

    # UPDATE: Returning a structured dictionary makes it much easier to plug into the Gradio UI
    return {
        "is_safe": is_safe,
        "extracted_drugs": extracted_drugs,
        "ddi_message": ddi_msg,
        "stewardship_message": abx_msg
    }


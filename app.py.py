"""Temperature scaling is used to calibrate predicted probabilities by adjusting model confidence without affecting accuracy."""

#how confident a model’s probabilities are without changing its predictions.
class ModelWithTemperature(nn.Module):
    """
    A thin wrapper around a model that performs temperature scaling on its outputs.
    """
    def __init__(self, model):
        super(ModelWithTemperature, self).__init__()
        self.model = model
        self.temperature = nn.Parameter(torch.ones(1) * 1.5) # Initialize T > 1 for potentially under-confident models

    def forward(self, input_ids, attention_mask):
        logits = self.model(input_ids=input_ids, attention_mask=attention_mask).logits
        return logits / self.temperature


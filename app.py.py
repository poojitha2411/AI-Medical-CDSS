def retrieve_top_k_dialogues(query_vector, corpus_embeddings, corpus, k=3):
    """
    Calculates cosine similarity, ranks results, and retrieves top-k relevant dialogues.
    """
    query_vector = query_vector.to(device)
    similarities = cosine_similarity(query_vector.cpu().numpy().reshape(1, -1), corpus_embeddings.cpu().numpy())
    similarities = similarities.flatten()
    sorted_indices = similarities.argsort()[::-1]

    top_k_results = []
    for i in range(k):
        idx = sorted_indices[i]
        top_k_results.append({
            "dialogue": corpus[idx],
            "similarity_score": similarities[idx]
        })
    return top_k_results


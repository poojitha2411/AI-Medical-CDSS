def focal_loss(logits, targets, alpha=1, gamma=2):
    ce_loss = F.cross_entropy(logits, targets, reduction='none')
    pt = torch.exp(-ce_loss)
    loss = (alpha * (1 - pt) ** gamma * ce_loss).mean()
    return loss

